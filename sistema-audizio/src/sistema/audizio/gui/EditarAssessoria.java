    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        String assessoria = null;
        if(tfAssessoria.getText().equals(assessoria)){
            JOptionPane.showMessageDialog(null, "PREENCHA OS CAMPOS CORRETAMENTE!");
        }else{
            Assessoria asses = new Assessoria();
            assessoria = tfAssessoria.getText();
            asses.setCidade(tfCidade.getText());
            asses.setBairro(tfBairro.getText());
            asses.setEndereco(tfEndereco.getText());
            asses.setId(idAssessoria);
            asses.setNome(assessoria);
            
            new DaoAssessoria().editar(asses);
               
            
        }
        tfAssessoria.setEnabled(false);
        tfCidade.setEnabled(false);
        tfBairro.setEnabled(false);
        tfEndereco.setEnabled(false);
        btCadastrar.setEnabled(false);
        preencheCampos(idAssessoria);
    }//GEN-LAST:event_btCadastrarActionPerformed

